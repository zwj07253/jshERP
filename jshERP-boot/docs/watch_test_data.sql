-- 35. 根据期初库存和全部有效业务流水统一重算当前库存
-- 测试环境关闭了强审核和出入库管理，因此所有未删除的有效流水均参与计算。
-- ============================================
WITH stock_keys AS (
    SELECT material_id, depot_id, tenant_id
    FROM jsh_material_initial_stock
    WHERE depot_id IS NOT NULL AND COALESCE(delete_flag, '0') != '1'
    UNION
    SELECT di.material_id, di.depot_id, dh.tenant_id
    FROM jsh_depot_item di
    INNER JOIN jsh_depot_head dh ON dh.id = di.header_id
    WHERE di.depot_id IS NOT NULL
      AND COALESCE(di.delete_flag, '0') != '1'
      AND COALESCE(dh.delete_flag, '0') != '1'
    UNION
    SELECT di.material_id, di.another_depot_id, dh.tenant_id
    FROM jsh_depot_item di
    INNER JOIN jsh_depot_head dh ON dh.id = di.header_id
    WHERE di.another_depot_id IS NOT NULL
      AND dh.sub_type = '调拨'
      AND COALESCE(di.delete_flag, '0') != '1'
      AND COALESCE(dh.delete_flag, '0') != '1'
)
INSERT INTO jsh_material_current_stock
    (material_id, depot_id, current_number, current_unit_price, tenant_id, delete_flag)
SELECT sk.material_id,
       sk.depot_id,
       0,
       COALESCE((
           SELECT me.purchase_decimal
           FROM jsh_material_extend me
           WHERE me.material_id = sk.material_id
             AND COALESCE(me.delete_flag, '0') != '1'
           ORDER BY (me.default_flag = '1') DESC, me.id
           LIMIT 1
       ), 0),
       sk.tenant_id,
       '0'
FROM stock_keys sk
WHERE sk.tenant_id = 100
  AND NOT EXISTS (
      SELECT 1
      FROM jsh_material_current_stock mcs
      WHERE mcs.material_id = sk.material_id
        AND mcs.depot_id = sk.depot_id
        AND mcs.tenant_id = sk.tenant_id
        AND COALESCE(mcs.delete_flag, '0') != '1'
  );

WITH stock_movement AS (
    -- 期初库存
    SELECT mis.material_id,
           mis.depot_id,
           mis.tenant_id,
           COALESCE(mis.number, 0) AS delta
    FROM jsh_material_initial_stock mis
    WHERE mis.depot_id IS NOT NULL
      AND COALESCE(mis.delete_flag, '0') != '1'

    UNION ALL

    -- 普通出入库、调拨出库、组装、拆卸和盘点复盘
    SELECT di.material_id,
           di.depot_id,
           dh.tenant_id,
           CASE
               WHEN dh.type = '入库' THEN COALESCE(di.basic_number, 0)
               WHEN dh.type = '出库' AND dh.sub_type != '调拨' THEN -COALESCE(di.basic_number, 0)
               WHEN dh.sub_type = '调拨' THEN -COALESCE(di.basic_number, 0)
               WHEN dh.sub_type = '组装单' AND di.material_type = '组合件' THEN COALESCE(di.basic_number, 0)
               WHEN dh.sub_type = '组装单' AND di.material_type = '普通子件' THEN -COALESCE(di.basic_number, 0)
               WHEN dh.sub_type = '拆卸单' AND di.material_type = '普通子件' THEN COALESCE(di.basic_number, 0)
               WHEN dh.sub_type = '拆卸单' AND di.material_type = '组合件' THEN -COALESCE(di.basic_number, 0)
               WHEN dh.sub_type = '盘点复盘' THEN COALESCE(di.basic_number, 0)
               ELSE 0
           END AS delta
    FROM jsh_depot_item di
    INNER JOIN jsh_depot_head dh ON dh.id = di.header_id
    WHERE di.depot_id IS NOT NULL
      AND COALESCE(di.delete_flag, '0') != '1'
      AND COALESCE(dh.delete_flag, '0') != '1'

    UNION ALL

    -- 调拨入库
    SELECT di.material_id,
           di.another_depot_id,
           dh.tenant_id,
           COALESCE(di.basic_number, 0) AS delta
    FROM jsh_depot_item di
    INNER JOIN jsh_depot_head dh ON dh.id = di.header_id
    WHERE di.another_depot_id IS NOT NULL
      AND dh.sub_type = '调拨'
      AND COALESCE(di.delete_flag, '0') != '1'
      AND COALESCE(dh.delete_flag, '0') != '1'
)
UPDATE jsh_material_current_stock mcs
SET current_number = COALESCE((
    SELECT SUM(sm.delta)
    FROM stock_movement sm
    WHERE sm.material_id = mcs.material_id
      AND sm.depot_id = mcs.depot_id
      AND sm.tenant_id = mcs.tenant_id
), 0)
WHERE mcs.tenant_id = 100
  AND COALESCE(mcs.delete_flag, '0') != '1';

