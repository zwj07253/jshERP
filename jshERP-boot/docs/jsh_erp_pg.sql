-- 2. jsh_account_head - 财务主表
-- ============================================================
DROP TABLE IF EXISTS jsh_account_head CASCADE;
CREATE TABLE jsh_account_head (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    type VARCHAR(50),
    organ_id BIGINT,
    hands_person_id BIGINT,
    creator BIGINT,
    change_amount DECIMAL(24,6),
    discount_money DECIMAL(24,6),
    total_price DECIMAL(24,6),
    account_id BIGINT,
    bill_no VARCHAR(50),
    bill_time TIMESTAMP,
    remark VARCHAR(1000),
    file_name VARCHAR(500),
    status VARCHAR(1),
