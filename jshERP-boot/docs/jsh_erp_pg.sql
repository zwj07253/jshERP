-- ============================================================
-- 1. jsh_account - 账户信息
-- ============================================================
DROP TABLE IF EXISTS jsh_account CASCADE;
CREATE TABLE jsh_account (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(50),
    serial_no VARCHAR(50),
    initial_amount DECIMAL(24,6),
    current_amount DECIMAL(24,6),
    remark VARCHAR(100),
    enabled BOOLEAN,
    sort VARCHAR(10),
    is_default BOOLEAN,
    tenant_id BIGINT,
    delete_flag VARCHAR(1) DEFAULT '0'
);
CREATE INDEX idx_account_tenant_id ON jsh_account(tenant_id);
COMMENT ON TABLE jsh_account IS '账户信息';
COMMENT ON COLUMN jsh_account.id IS '主键';
COMMENT ON COLUMN jsh_account.name IS '名称';
COMMENT ON COLUMN jsh_account.serial_no IS '编号';
COMMENT ON COLUMN jsh_account.initial_amount IS '期初金额';
COMMENT ON COLUMN jsh_account.current_amount IS '当前余额';
COMMENT ON COLUMN jsh_account.remark IS '备注';
COMMENT ON COLUMN jsh_account.enabled IS '启用';
COMMENT ON COLUMN jsh_account.sort IS '排序';
COMMENT ON COLUMN jsh_account.is_default IS '是否默认';
COMMENT ON COLUMN jsh_account.tenant_id IS '租户id';
COMMENT ON COLUMN jsh_account.delete_flag IS '删除标记，0未删除，1删除';

SELECT setval('jsh_account_id_seq', 1);

